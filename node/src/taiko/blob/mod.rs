    #[test]
    fn test_build_taiko_blob_sidecar() {
        let data = vec![3u8; 400];
        let sidecar =
            build_taiko_blob_sidecar(&data).expect("assert: can build taiko blob sidecar");
        for s in sidecar.into_iter() {
            assert!(s.verify_blob_kzg_proof().is_ok());
        }
    }

